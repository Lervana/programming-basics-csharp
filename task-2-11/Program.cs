// 2.11.Napisz program, który sprawdza, czy podana liczba całkowita jest parzysta
//      czy nieparzysta, i wyświetla odpowiedni komunikat.

int liczba = 10;

if (liczba % 2 == 0) {
    Console.WriteLine($"Liczba {liczba} jest liczbą parzystą.");
} else {
    Console.WriteLine($"Liczba {liczba} jest liczbą nieparzystą.");
}
